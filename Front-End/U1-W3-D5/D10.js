function howManyDays(dataInserita) {
  const now = new Date();
  const data = new Date(dataInserita);
  const giorniTrascorsi = (now - data) / (1000 * 60 * 60 * 24); // sono i millisecondi in un giorno
  console.log("sono passati " + giorniTrascorsi + " giorni");
  return giorniTrascorsi;
}
